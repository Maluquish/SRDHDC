package pe.unmsm.srdhdc.domain.dto;

public class Escuela {
	private String cod_esc;
	private String cod_fac;
	private String des_esc;

	public String getCod_esc() {
		return cod_esc;
	}

	public void setCod_esc(String cod_esc) {
		this.cod_esc = cod_esc;
	}

	public String getCod_fac() {
		return cod_fac;
	}

	public void setCod_fac(String cod_fac) {
		this.cod_fac = cod_fac;
	}

	public String getDes_esc() {
		return des_esc;
	}

	public void setDes_esc(String des_esc) {
		this.des_esc = des_esc;
	}
}
